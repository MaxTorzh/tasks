package ru.yandex.practicum.oop.rent.model;

public abstract class Vehicle {
    private int id;
    private String model;

    public Vehicle(int id) {
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }
}
